for c in range (6 , 0, -1):
# Na contagem de 6 até 1 se não colocarmos o -1 o programa não executa o que está dentro do for 
    print(c)
print('FIM')

for c in range (0, 7):
# Contando de 0 a 6
    print(c)
print('FIM')

for c in range (0 , 7 , 2):
# O programa conta de 0 a 6 de dois  em dois
    print(c)
