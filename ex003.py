n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))

#Colocando o print com o cálculo dentro do .format para não criar muitas váriaveis.

print('A soma entre os números digitados são: {}'.format(n1+n2))
print('A subtração do números digitados são: {}'.format(n1-n2))
print('A multiplicação dos números digitados são: {}'.format(n1*n2))
print('A divisão dos números digitados são: {}'.format(n1/n2))